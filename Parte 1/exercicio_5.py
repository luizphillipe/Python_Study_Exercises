'''5. Programa para calcular a potência de dois números com entrada do usuário.'''

valor1 = float(input("Digite o valor 1: "))
valor2 = float(input("Digite o valor 2: "))
operacao_potencia = (valor1**valor2)

nome = (input("Qual seu nome?: "))

print(f"{nome}, A potencia dos numeros {valor1} e {valor2} resulta em: {operacao_potencia}")

